import statistics
myListMean = statistics.mean([34, 23, 54, 43, 53, 43, 99, 43, 74, 57, 82, 80, 17])
print("The mean is: ", myListMean)

myListMedian = statistics.median([34, 23, 54, 43, 53, 43, 99, 43, 74, 57, 82, 80, 17])
print("The median is: ", myListMedian)

myListMode = statistics.mode([34, 23, 54, 43, 53, 43, 99, 43, 74, 57, 82, 80, 17])
print("The mode is: ", myListMode)
